
#ifndef __mytypes_h
#define __mytypes_h

#ifndef __IGNORE_TYPES
#ifndef _INT8_T
#define _INT8_T 
typedef char              int8_t;
#endif
                               
#ifndef _INT16_T
#define _INT16_T 
typedef  short            int16_t;
#endif
                               
#ifndef _INT32_T
#define _INT32_T 
typedef  int              int32_t;
#endif
#endif
typedef  unsigned short u_int16_t;
typedef unsigned char   u_int8_t; 
typedef  unsigned int   u_int32_t;
#endif//__mytypes_h
